'''The length 'L' and breadth 'B' of a rectangle are passed as input. The program must calculate
the perimeter of the rectangle and print the output. Both L and B are positive integers.
Output displays the calculated perimeter'''
l=int(input("enter the length of the rectangle"))
b=int(input("enter the breadth of the rectangle"))
